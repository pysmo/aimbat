c . . . . . . . . . BEGIN SUBROUTINE GETIME . . . . . . . . . . . . . .
      subroutine getime(modnam, phase, slat, slon, elat, elon, evdp,
     &   time, elcr, sdtdd, ie, id, ic)
c . .
c . . Input:
c . .   phase      : ttimes phase (e.g. P, PKPdf, pP, SKKSac, ...)
c . .   slat, slon : station latitude and longitude
c . .   elat, elon : earthquake latitude and longitude
c . .   evdp       : earthquake depth in km
c . .   ie         : ellipticity flag:
c . .                 1 = ignore ellipticity
c . .                 0 = calculate ellipticity
c . .                ellipticity corrections available for phases: 
c . .                 P PcP PKPab PKPbc PKPdf S ScS SKSac SKSdf ScP SKP
c . .   id         : calculate tables for new e.q. depth if zero
c . .   ic         : coordinates are taken as geographic degrees if
c . .                 nonzero, or geocentric radians if ic is zero
c . .
c . . Output:
c . .   time : travel time from earthquake to station (in seconds)
c . .   elcr : ellipticity correction (if available for given phase)
c . .
c . .               NO ELEVATION CORRECTIONS ARE MADE
c . .
c . .   questions to: John VanDecar (vandecar@dtm.ciw.edu)  -  5/95
c . .

      implicit none 

      character*8 phase
      real slat, slon, elat, elon, evdp, time, elcr, sdtdd
      integer ic, id, ie, ier

c . . parameters
      real pi, f
      parameter(pi=3.14159265)
      parameter(f=180.0/pi)

c . . local variables
      logical lopen
      integer i
      real stla, stlo, evla, evlo, dum, delta, azim

c . . ttimes IASP91 variables
      integer n, max
      parameter (max=60)
      logical prnt(3)
      real usrc(2)
      character*8 phcd(max), phlst(10)
      real tt(max),dtdd(max), dtdh(max), dddp(max)

! . . 1d reference model from input
!      include 'getime.h'
      integer in
      character*90 modnam
      data in/12/

!f2py intent(in)  :: modnam, phase, slat, slon, elat, elon, evdp, ie, id, ic
!f2py intent(out) :: time, elcr, sdtdd

c . . calculate travel time of first arriving P and S waves
c . .  through IASP91 velocity model (no elipticity corrections)

      if(ic.ne.0)then
c . . . transfer earthquake and station locations to geocentric radians
        call coortr (evla, evlo, elat, elon, 0)
        call coortr (stla, stlo, slat, slon, 0)
      else
        evla = elat
        evlo = elon
        stla = slat
        stlo = slon
      endif

c . . if id is non-zero then earthquake is at same depth as in previous 
c . .   call, and there is no need to re-calculate tables
      if(id.eq.0)then
        prnt(1) = .false.
        prnt(2) = .false.
        prnt(3) = .false.

        call assign(10,2,'/dev/null')
        call tabin(in,modnam)
        phlst(1) = 'ALL     '
        call brnset(1,phlst,prnt)

c . . . set depth
        call depset(evdp,usrc)
      endif

c . . calculate angular distance and azimuth
      call delaz(evla, evlo, dum, dum, dum, 0)
      call delaz(stla, stlo, delta, azim, dum, 1)
      delta = delta*f
      azim = azim*f
c . . find times to all phases
      call trtm(delta, max, n, tt, dtdd, dtdh, dddp, phcd)

c . . find phase and time in list
      lopen = .true.
      do 10 i=1,n
        if(lopen.and.phcd(i).eq.phase)then
          lopen = .false.
          time = tt(i)
          sdtdd = dtdd(i)
        endif
 10   continue
      if(lopen)then
        write(0,'(a,f5.1,a)')'getime: ERROR: phase '//phase//'not'//
     &     ' found in IASP91 tables at distance: ',delta,'. stop.'
        write(0,'(a,f5.1,a,f5.1)')'station latitude: ',slat,
     &     ', and longitude: ',slon
c        stop
      endif

c . . calculate ellipticity correction from Dziewonski and
c . .  Gilbert's (1976) paper (Kennett's tables)
      if(ie.eq.0)then
        call elpcor(phase,delta,evdp,azim,90.-evla*f,elcr,ier)
        if(ier.ne.0)then
          write(0,'(a,i1)')'getime: ERROR: in elpcor, ier = ',ier
          write(0,'(a,f5.1,a)')
     &       'getime: no ellipticity correction made for'//
     &       ' distance: ',delta,', and phase: '//phase
          write(0,'(a,f5.1,a,f5.1)')'station latitude: ',slat,
     &       ', and longitude: ',slon
        endif
      endif

      return
      end

