c	on the source to receiver distance and azimuth, and the source
c	depth and colatitude.  The correction scheme was developed by
c	Dziewonski and Gilbert (Geophys. J. R. astr. Soc, 44, 7-17, 1976),
c	and the tabulated values were developed by Brian Kennett (IASPEI
c	1991 Seismological Tables, B.L.N. Kennett, Editor, Research
c	School of Earth Sciences, Australian National University, p149-163).
