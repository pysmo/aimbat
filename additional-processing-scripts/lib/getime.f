	ecorr = 0.0
	t0 = 0.0
	t1 = 0.0
	t2 = 0.0
	azir = azi * degrad
	ecolatr = ecolat * degrad
c
c ----	Get the index for the phase name.  If no table for the phase, return.
c
	if(phid(1:2).eq.'P '.or.phid(1:3).eq.'Pn '.or.phid(1:3).eq.'Pg ' 
     &		.or. phid(1:3).eq.'Pb ' .or. phid(1:3).eq.'Pdi') then
	  ips = 1
	else if (phid(1:4) .eq. 'PcP ') then
	  ips = 2
	else if (phid(1:6) .eq. 'PKPab ') then
	  ips = 3
	else if (phid(1:6) .eq. 'PKPbc ') then
	  ips = 4
	else if (phid(1:6) .eq. 'PKPdf ') then
          ips = 5
	else if (phid(1:6) .eq. 'PKiKP  ') then
	  ips = 5
	else if(phid(1:2).eq.'S '.or.phid(1:3).eq.'Sn '.or.phid(1:3).eq. 
     &		'Sg '.or. phid(1:3).eq.'Sb ' .or. phid(1:3).eq.'Sdi') then
	  ips = 6
	else if (phid(1:4) .eq. 'ScS ') then
	  ips = 7
	else if (phid(1:6) .eq. 'SKSac ') then
	  ips = 8
	else if (phid(1:6) .eq. 'SKSdf ') then
	  ips = 9
	else if (phid(1:4) .eq. 'ScP ') then
	  ips = 10
	else if (phid(1:4) .eq. 'SKP ') then
	  ips = 11
	else
          ierr=1
	  return
	endif
c
c ----  check depth and distance to be within limits
c
	if( del .lt. delmin(ips) .or. del .gt. delmax(ips) ) then
          ierr=2
          return
        endif
	if( z .lt. 0.0 .or. z .gt. 700 ) then
          ierr=3
          return
        endif
c
c
c ----  find the depth index for the value just less than the input
c
	do i = 2,8
	  if(z.le.depth(i)) then
	    idep = i-1
	    goto 2
	  endif
	enddo
c
c ----  idel is the depth index just less than the input depth
c ----  dd is the fractional change in distance between the input distance
c       and the next closest depth index
c ----  dz is the fractional change in depth between the depth at idel
c       and the input depth
c
2	delta = del - delmin(ips)
	idel = 1 + int((delta+0.0001)/delinc)
	idel = min( idel,int((delmax(ips)-delmin(ips))/delinc) )
	dd = 1.0 + (delta/delinc)  - float(idel)
	dz = (z - depth(idep)) / (depth(idep+1) - depth(idep))
c
c ----  compute the three t terms using bilinear interpolation
c
	idel1 = idel + 1
	idep1 = idep + 1
	do i = 1,3
	  t(i) = (1.0-dz)*    dd   * tau(i,idel1,idep,ips)    + 
     +               dz  *    dd   * tau(i,idel1,idep1,ips)   +
     +               dz  *(1.0-dd) * tau(i,idel,idep1,ips)    +
     +           (1.0-dz)*(1.0-dd) * tau(i,idel,idep,ips)
	enddo
c
c ----  compute the correction
c
	anumber = 0.86602540375
	ecorr = 0.25*(1.0 + 3.0*cos(2.0*ecolatr))*t(1) +
     +          anumber*sin(2.0*ecolatr)*cos(azir)*t(2) +
     +          anumber*sin(ecolatr)*sin(ecolatr)*cos(2.0*azir)*t(3)
c
c ----	all done
c
        ierr=0
	return
	end
